#include <iostream>
#include <stdio.h>
#include <stdlib.h>
using namespace std;
int main() {
	const int size = 100;//задаем размер массива
	char str[size];//создаем массив под строку
	char ch;//переменная под символ
	cout << "\n\nEnter line " << endl;
	gets_s(str);//записываем введенную строку в массив
	cout << "\n\nEnter symbol " << endl;
	cin >> ch;//записываем введенный символ в переменную
	int k = 0;// переменная для записи индекса массива при совпадении
	for (int i = 0; i < size; i++) {
		if (str[i] == ch) {
			k = i;//при каждом совпадении присваиваем значение индекса массива
		}
		else if (str[i] == '\0') {//при достижении конца строки выходим из цикла
			break;
		}
	}
	if (k == 0) {
		cout << "\n\nNot found";//если не было совпадений выводим сообщение
	}
	else {
		cout << "\n\nlast coincidence position : " << (k + 1) << endl;//если совпадения были выводим последнюю позицию
	}
	int end_;
	cin >> end_;
	return (0);
}